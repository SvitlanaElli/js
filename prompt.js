let age = prompt("Введіть свій вік:");

// Перевірка чи введене значення є числом і не від'ємне
if (!isNaN(age) && age >= 0) {
  // Визначення правильного слова для виводу
  let word;
  if (age % 10 === 1 && age % 100 !== 11) {
    word = "рік";
  } else if ((age % 10 >= 2 && age % 10 <= 4) && (age % 100 < 10 || age % 100 >= 20)) {
    word = "роки";
  } else {
    word = "років";
  }

  // Виведення результату
  console.log(`${age} ${word}`);
} else {
  console.log("Будь ласка, введіть додатнє число.");
}